package com.tfg.terranostra.controllers;

import com.tfg.terranostra.dto.CambioContraseniaDto;
import com.tfg.terranostra.dto.LoginDto;
import com.tfg.terranostra.dto.TokenGoogleDto;
import com.tfg.terranostra.dto.UsuarioDto;
import com.tfg.terranostra.repositories.UsuarioRepository;
import com.tfg.terranostra.services.AuthService;
import com.tfg.terranostra.services.PasswordResetTokenService;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

@RestController
@RequestMapping("/api/auth")
@CrossOrigin(origins = "*")

/**
 * Controlador para la autenticaci√≥n de usuarios en la aplicaci√≥n.
 * Proporciona endpoints para el inicio de sesi√≥n de los usuarios.
 *
 * @author ebp
 * @version 1.0
 */
public class AuthController {

    private static final Logger logger = LoggerFactory.getLogger(AuthController.class);

    @Autowired
    private UsuarioRepository usuarioRepository;

    @Autowired
    private AuthService authService;

    @Autowired
    private PasswordResetTokenService passwordResetTokenService;


    /**
     * Maneja el inicio de sesi√≥n de los usuarios.
     * - Recibe un objeto `LoginDto` con las credenciales del usuario.<br>
     * - Si las credenciales son correctas, devuelve un objeto `UsuarioDto` con los datos del usuario autenticado.<br>
     * - Si las credenciales son incorrectas, devuelve un c√≥digo de estado HTTP 401 con un mensaje de error.
     *
     * @param loginDto Objeto que contiene el correo electr√≥nico y la contrase√±a del usuario.
     * @return `ResponseEntity` con el objeto `UsuarioDto` si la autenticaci√≥n es exitosa,
     *         o un mensaje de error si las credenciales son incorrectas.
     */

    @PostMapping("/login")
    public ResponseEntity<?> inicioSesion(@RequestBody LoginDto loginDto) {
        logger.info("üîê Intento de inicio de sesi√≥n para el usuario: {}", loginDto.getEmail());

        UsuarioDto usuarioDto = authService.autenticarUsuario(loginDto);

        if (usuarioDto == null) {
            logger.warn("‚ùå Inicio de sesi√≥n fallido: Credenciales incorrectas para el usuario {}", loginDto.getEmail());
            return ResponseEntity.status(401).body("‚ùå Credenciales incorrectas");
        }

        logger.info("‚úÖ Inicio de sesi√≥n exitoso para el usuario: {}", loginDto.getEmail());
        logger.debug("üì§ Respuesta JSON enviada por la API: {}", usuarioDto);

        return ResponseEntity.ok(usuarioDto);
    }

    /**
     * Valida si un token de restablecimiento de contrase√±a es v√°lido.
     * - El token se pasa como par√°metro en la URL.
     * - El servicio valida si el token existe, no ha expirado y est√° asociado a un usuario.
     *
     * @param token El token de restablecimiento de contrase√±a.
     * @return `ResponseEntity` con `true` si el token es v√°lido, o `false` si no lo es.
     */

    @GetMapping("/validar-token")
    public ResponseEntity<Boolean> validarToken(@RequestParam String token) {
        boolean esValido = passwordResetTokenService.validarToken(token);
        return ResponseEntity.ok(esValido);
    }

    /**
     * Cambia la contrase√±a de un usuario usando un token de restablecimiento.
     * - El token y la nueva contrase√±a se reciben en el cuerpo del mensaje.
     * - El servicio valida el token y actualiza la contrase√±a si es v√°lido.
     *
     * @param cambioContraseniaDto Objeto que contiene el token y la nueva contrase√±a.
     * @return `ResponseEntity` con un mensaje indicando si el cambio fue exitoso o no.
     */

    @PutMapping("/cambiar-password")
    public ResponseEntity<String> cambiarContrasenia(@RequestBody CambioContraseniaDto cambioContraseniaDto) {
        boolean exito = authService.cambiarContrasenia(cambioContraseniaDto);

        if (exito) {
            return ResponseEntity.ok("‚úÖ Contrase√±a cambiada exitosamente.");
        } else {
            return ResponseEntity.status(HttpStatus.BAD_REQUEST).body("‚ùå Token inv√°lido o expirado.");
        }
    }

    /**
     * Autentica a un usuario usando un token de Google OAuth2.
     * - Recibe un token de Google en el cuerpo de la petici√≥n.
     * - Si el token es v√°lido y el usuario existe (o se crea), se devuelve un `UsuarioDto` con la sesi√≥n iniciada.
     *
     * @param tokenGoogleDto Objeto que contiene el token JWT emitido por Google.
     * @return `ResponseEntity` con la informaci√≥n del usuario autenticado, o un error si falla el proceso.
     */

    @PostMapping("/login-google")
    public ResponseEntity<?> loginConGoogle(@RequestBody TokenGoogleDto tokenGoogleDto) {
        try {
            return authService.loginConGoogle(tokenGoogleDto);
        } catch (Exception e) {
            return ResponseEntity.badRequest().body("Error en el login con Google: " + e.getMessage());
        }
    }
}
